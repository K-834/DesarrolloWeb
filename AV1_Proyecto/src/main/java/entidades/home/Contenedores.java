package entidades.home;

/**
 *
 * @author antho
 */
public class Contenedores {
    
    private String titulo;
    private String imagen;
    private String link;

    public Contenedores(String titulo, String imagen, String link) {
        this.titulo = titulo;
        this.imagen = imagen;
        this.link = link;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }
    
}
